#include "libft.h"

char	*ft_itoa(int n)
{
	int		i[3];
	char	*ret;

	if (n == -2147483648)
		return (ft_strdup("-2147483648"));
	i[0] = 1;
	i[1] = 1;
	if (n < 0)
	{
		i[0] = -1;
		i[1]++;
	}
	n *= i[0];
	i[2] = n;
	while ((i[2] = i[2] / 10) >= 1)
		i[1]++;
	ret = ft_strnew(i[1] + 1);
	i[2] = n;
	while (i[1] >= 0)
	{
		ret[--i[1]] = (i[2] % 10) + 48;
		i[2] = i[2] / 10;
	}
	return ((i[0] < 0 ? ft_strncpy(ret, "-", 1) : ret));
}
